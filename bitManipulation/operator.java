// bitwise operator

public class operator {
    public static void main(String[] args) {
        System.out.println("AND operator of 5&6 : " +(5&6));
        System.out.println("OR operator of 5|6 : " +(5|6));
        System.out.println("XOR operator of 5^6 : " +(5^6));
        System.out.println("One's Compliment operator of ~5 : " +(~5));
        System.out.println("Left Shift operator of 5<<2 : " +(5<<2));
        System.out.println("Right Shift operator of 6>>1 : " +(6>>1));
    }
    
}
